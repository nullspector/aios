@app.command()
def notebook(
    port: int = typer.Option(8888, "--port", "-p"),
    no_browser: bool = typer.Option(False, "--no-browser"),
    directory: Optional[str] = typer.Option(None, "--dir", "-d"),
) -> None:
    """Launch JupyterLab."""
    if not shutil.which("jupyter"):
        console.print("[red]Jupyter not found.[/red]  Install: [bold]pip install jupyterlab[/bold]")
        raise typer.Exit(1)
    root = directory or str(AIOS_PROJECTS_DIR)
    Path(root).mkdir(parents=True, exist_ok=True)
    cmd = ["jupyter", "lab", f"--port={port}", f"--notebook-dir={root}"]
    if no_browser:
        cmd.append("--no-browser")
    console.print(f"[bold cyan]Starting JupyterLab[/bold cyan]  →  http://localhost:{port}")
    os.execvp("jupyter", cmd)


