@app.command()
def gpu(
    watch:   bool = typer.Option(False, "--watch", "-w"),
    as_json: bool = typer.Option(False, "--json"),
) -> None:
    """Show GPU utilisation, VRAM, temperature, and power."""

    def _collect() -> dict:
        if not shutil.which("nvidia-smi"):
            return {"vendor": None, "gpus": []}
        code, out, _ = run([
            "nvidia-smi",
            "--query-gpu=index,name,driver_version,utilization.gpu,utilization.memory,"
            "memory.used,memory.total,temperature.gpu,power.draw,power.limit",
            "--format=csv,noheader,nounits",
        ])
        if code != 0:
            return {"vendor": "NVIDIA", "gpus": []}
        gpus = []
        for line in out.splitlines():
            p = [x.strip() for x in line.split(",")]
            if len(p) < 10:
                continue
            gpus.append({
                "index": int(p[0]), "name": p[1], "driver": p[2],
                "gpu_util":    float(p[3]) if p[3].replace(".", "").isdigit() else 0,
                "mem_used":    int(p[5])   if p[5].isdigit() else 0,
                "mem_total":   int(p[6])   if p[6].isdigit() else 0,
                "temp":        int(p[7])   if p[7].isdigit() else 0,
                "power_draw":  float(p[8]) if p[8].replace(".", "").isdigit() else 0,
                "power_limit": float(p[9]) if p[9].replace(".", "").isdigit() else 0,
            })
        return {"vendor": "NVIDIA", "gpus": gpus}

    def _bar(pct: int, w: int = 20) -> str:
        f = round(pct / 100 * w)
        return "█" * f + "░" * (w - f)

    def _render(data: dict) -> None:
        if not data["gpus"]:
            console.print(Panel("[yellow]No GPU detected — CPU-only mode[/yellow]",
                                title="AIOS GPU", border_style="yellow"))
            return
        for g in data["gpus"]:
            mem_pct = round(g["mem_used"] / max(g["mem_total"], 1) * 100)
            tc = "red" if g["temp"] > 80 else "yellow" if g["temp"] > 70 else "green"
            body = (
                f"[bold white]GPU {g['index']}:[/bold white] [cyan]{g['name']}[/cyan]  "
                f"[dim]Driver {g['driver']}[/dim]\n\n"
                f"  [dim]GPU   [/dim] [{_bar(int(g['gpu_util']))}] [bold yellow]{g['gpu_util']:.0f}%[/bold yellow]\n"
                f"  [dim]VRAM  [/dim] [{_bar(mem_pct)}] [bold yellow]{g['mem_used']:,}/{g['mem_total']:,} MB[/bold yellow]"
                f"  [dim]({mem_pct}%)[/dim]\n"
                f"  [dim]Temp  [/dim] [{_bar(g['temp'], 10)}] [{tc}]{g['temp']}°C[/{tc}]\n"
                f"  [dim]Power [/dim] [{_bar(round(g['power_draw']/max(g['power_limit'],1)*100))}] "
                f"[bold]{g['power_draw']:.0f}W / {g['power_limit']:.0f}W[/bold]\n"
            )
            console.print(Panel(body, title="[bold magenta]AIOS GPU[/bold magenta]",
                                border_style="magenta", padding=(0, 2)))

    if watch:
        try:
            while True:
                console.clear()
                data = _collect()
                print(json.dumps(data, indent=2)) if as_json else _render(data)
                console.print("[dim]  Ctrl+C to stop[/dim]")
                time.sleep(2)
        except KeyboardInterrupt:
            console.print("\n[dim]Stopped.[/dim]")
    else:
        data = _collect()
        print(json.dumps(data, indent=2)) if as_json else _render(data)


