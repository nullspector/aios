DOCKER_IMAGES = {
    "nlp":  "aiosproject/aios-nlp:latest",
    "cv":   "aiosproject/aios-cv:latest",
    "llm":  "aiosproject/aios-llm:latest",
    "base": "aiosproject/aios-base:latest",
}


@docker.command("run")
def docker_run(
    env_type: str = typer.Argument(..., help=f"Container type: {', '.join(DOCKER_IMAGES)}"),
    port: int     = typer.Option(8888, "--port", "-p"),
    gpu: bool     = typer.Option(True, "--gpu/--no-gpu"),
) -> None:
    """Start an AIOS Docker AI environment."""
    if env_type not in DOCKER_IMAGES:
        console.print(f"[red]Unknown type '{env_type}'. Choose: {', '.join(DOCKER_IMAGES)}[/red]")
        raise typer.Exit(1)
    if not shutil.which("docker"):
        console.print("[red]Docker not found.[/red]  Run [bold]./install.sh[/bold] first.")
        raise typer.Exit(1)
    image = DOCKER_IMAGES[env_type]
    name  = f"aios-{env_type}"
    run(["docker", "rm", "-f", name])
    cmd = ["docker", "run", "--detach", "--name", name,
           "--publish", f"{port}:8888",
           "--volume", f"{AIOS_PROJECTS_DIR}:/workspace"]
    if gpu:
        cmd += ["--gpus", "all"]
    cmd.append(image)
    console.print(f"[bold cyan]Starting:[/bold cyan] {name}  →  http://localhost:{port}")
    with Progress(SpinnerColumn(), TextColumn("{task.description}"), console=console) as p:
        task = p.add_task(f"Pulling {image}...", total=None)
        code, out, err = run(cmd, timeout=120)
        p.stop()
    if code == 0:
        console.print(f"[green]✓[/green] {name} started  [dim](ID: {out[:12]})[/dim]")
    else:
        console.print(f"[red]✗ Failed:[/red] {err}")
        raise typer.Exit(1)


@docker.command("stop")
def docker_stop(name: str = typer.Argument(...)) -> None:
    """Stop a running AIOS container."""
    code, _, err = run(["docker", "stop", name], timeout=15)
    console.print(f"[green]✓[/green] Stopped {name}" if code == 0 else f"[red]✗[/red] {err}")


@docker.command("list")
def docker_list() -> None:
    """List running AIOS containers."""
    code, out, _ = run(["docker", "ps", "--format", "{{.Names}}\t{{.Image}}\t{{.Status}}\t{{.Ports}}"])
    t = Table(box=box.ROUNDED, border_style="dim", header_style="bold cyan")
    t.add_column("Name"); t.add_column("Image"); t.add_column("Status"); t.add_column("Ports")
    if code == 0 and out:
        for line in out.splitlines():
            parts = line.split("\t")
            t.add_row(*parts[:4]) if len(parts) >= 4 else None
    else:
        t.add_row("[dim]No containers running[/dim]", "", "", "")
    console.print(t)


