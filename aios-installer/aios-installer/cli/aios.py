@app.command()
def doctor(
    category: Optional[str] = typer.Option(None, "--category", "-c",
        help="core | ml | gpu | docker | jupyter | system"),
    as_json: bool = typer.Option(False, "--json"),
    quiet:   bool = typer.Option(False, "--quiet", "-q"),
) -> None:
    """Run a full environment health check."""

    def _chk_python():
        v = sys.version_info
        ok = v >= (3, 10)
        return ok, f"Python {v.major}.{v.minor}.{v.micro}" + ("" if ok else " (3.10+ required)")

    def _chk_cmd(cmd):
        if not shutil.which(cmd):
            return False, f"'{cmd}' not found"
        code, out, err = run([cmd, "--version"])
        txt = (out or err).splitlines()[0][:60] if (out or err) else "ok"
        return code == 0, txt

    def _chk_pkg(pkg, imp=None):
        imp = imp or pkg.replace("-", "_")
        code, out, err = run([py_cmd(), "-c",
            f"import {imp}; v=getattr({imp},'__version__','ok'); print(v)"], timeout=15)
        return code == 0, (out or err.strip()[:60])

    def _chk_docker():
        if not shutil.which("docker"):
            return False, "Not installed"
        code, out, _ = run(["docker", "info", "--format", "{{.ServerVersion}}"])
        return code == 0, f"Docker {out}" if code == 0 else "Daemon not running"

    def _chk_nvidia():
        if not shutil.which("nvidia-smi"):
            return False, "nvidia-smi not found"
        code, out, _ = run(["nvidia-smi", "--query-gpu=name,driver_version", "--format=csv,noheader"])
        return code == 0, out.splitlines()[0][:60] if code == 0 else "nvidia-smi failed"

    def _chk_cuda():
        code, out, _ = run([py_cmd(), "-c",
            "import torch; c=torch.cuda.is_available(); d=torch.cuda.device_count(); "
            "print(f'CUDA={c} devices={d}')"], timeout=30)
        if code != 0:
            return False, "PyTorch not installed"
        return out.startswith("CUDA=True"), out.strip()

    def _chk_disk():
        t, u, f = shutil.disk_usage("/")
        gb = f // (2**30)
        return gb >= 5, f"{gb} GB free" + ("" if gb >= 5 else " (5 GB+ recommended)")

    CHECKS = [
        ("Python 3.10+",   "core",    _chk_python,                          True),
        ("pip",            "core",    lambda: _chk_cmd("pip"),               True),
        ("git",            "core",    lambda: _chk_cmd("git"),               True),
        ("Docker",         "docker",  _chk_docker,                           False),
        ("PyTorch",        "ml",      lambda: _chk_pkg("torch"),             False),
        ("Transformers",   "ml",      lambda: _chk_pkg("transformers"),      False),
        ("NumPy",          "ml",      lambda: _chk_pkg("numpy"),             False),
        ("pandas",         "ml",      lambda: _chk_pkg("pandas"),            False),
        ("NVIDIA GPU",     "gpu",     _chk_nvidia,                           False),
        ("PyTorch + CUDA", "gpu",     _chk_cuda,                             False),
        ("JupyterLab",     "jupyter", lambda: _chk_cmd("jupyter"),           False),
        ("Disk Space",     "system",  _chk_disk,                             False),
    ]

    checks = [(n, c, fn, crit) for n, c, fn, crit in CHECKS
              if category is None or c == category.lower()]

    results: dict = {}
    passed = failed = 0

    with Progress(SpinnerColumn(), TextColumn("{task.description}"), console=console, transient=True) as p:
        task = p.add_task("Checking...", total=len(checks))
        for name, cat, fn, critical in checks:
            p.update(task, description=f"Checking {name}...")
            ok_flag, msg = fn()
            results[name] = {"category": cat, "ok": ok_flag, "msg": msg, "critical": critical}
            passed += ok_flag
            failed += not ok_flag
            p.advance(task)

    if as_json:
        print(json.dumps({"passed": passed, "failed": failed, "results": results}, indent=2))
        return

    cats = list(dict.fromkeys(c for _, c, _, _ in checks))
    for cat in cats:
        cat_results = {n: r for n, r in results.items() if r["category"] == cat}
        if quiet and all(r["ok"] for r in cat_results.values()):
            continue
        t = Table(box=box.ROUNDED, border_style="dim", header_style="bold cyan", min_width=64)
        t.add_column("Component", style="bold white", min_width=20)
        t.add_column("Status", justify="center", min_width=8)
        t.add_column("Detail", style="dim", min_width=36)
        for name, r in cat_results.items():
            if quiet and r["ok"]:
                continue
            icon = "[green]  ✓  [/green]" if r["ok"] else "[red]  ✗  [/red]"
            t.add_row(name, icon, r["msg"])
        console.print(f"\n[bold cyan]{cat.upper()}[/bold cyan]")
        console.print(t)

    console.print()
    console.print(Panel(
        f"[green]Passed: {passed}[/green]   [red]Failed: {failed}[/red]\n\n" + (
            "[green]🎉 Environment looks great![/green]"
            if failed == 0 else
            f"[yellow]⚠  {failed} check(s) need attention.[/yellow]\n"
            "[dim]Run [bold]aios wizard[/bold] for automated fixes.[/dim]"
        ),
        title="[bold magenta]AIOS Doctor[/bold magenta]",
        border_style="green" if failed == 0 else "yellow",
    ))
    raise typer.Exit(0 if failed == 0 else 1)


