@app.command()
def new(
    name: str = typer.Argument(..., help="Project name (creates ~/aios-projects/<name>)"),
    template: str = typer.Option(
        "blank", "--template", "-t",
        help=f"Project template: {', '.join(TEMPLATES)}",
    ),
    no_venv:    bool = typer.Option(False, "--no-venv",    help="Skip venv creation"),
    no_install: bool = typer.Option(False, "--no-install", help="Skip pip install"),
) -> None:
    """
    Create a new AI project from a template.

    \b
    Examples:
      aios new my-model
      aios new bert-finetune --template nlp
      aios new rag-app --template llm --no-venv
    """
    if template not in TEMPLATES:
        console.print(f"[red]Unknown template '{template}'. Available: {', '.join(TEMPLATES)}[/red]")
        raise typer.Exit(1)

    project_dir = AIOS_PROJECTS_DIR / name
    if project_dir.exists():
        console.print(f"[red]Project '{name}' already exists at {project_dir}[/red]")
        raise typer.Exit(1)

    tmpl = TEMPLATES[template]
    console.print(LOGO)
    console.print(f"[bold cyan]Creating project:[/bold cyan] {name}")
    console.print(f"[dim]Template:[/dim] {template} — {tmpl['description']}")
    console.print(f"[dim]Location:[/dim] {project_dir}\n")

    AIOS_PROJECTS_DIR.mkdir(parents=True, exist_ok=True)
    project_dir.mkdir(parents=True)
    for d in tmpl["dirs"]:
        (project_dir / d).mkdir(parents=True, exist_ok=True)
        (project_dir / d / ".gitkeep").touch()

    (project_dir / ".aios-env").write_text(
        f"template={template}\n"
        f"created={datetime.now().isoformat()}\n"
        f"name={name}\n"
        f"aios_version={AIOS_VERSION}\n"
    )

    nb_stub = json.dumps({
        "nbformat": 4, "nbformat_minor": 5,
        "metadata": {"kernelspec": {"display_name": "Python 3", "language": "python", "name": "python3"}},
        "cells": [
            {"cell_type": "markdown", "metadata": {}, "source": [f"# {name}\n\nGenerated by AIOS  \nTemplate: `{template}`"]},
            {"cell_type": "code", "execution_count": None, "metadata": {}, "outputs": [], "source": ["# Your code here\n"]},
        ],
    }, indent=2)
    for nb in tmpl["notebooks"]:
        (project_dir / nb).write_text(nb_stub)

    (project_dir / "README.md").write_text(
        f"# {name}\n\n> Template: `{template}` — {tmpl['description']}\n\n"
        f"## Setup\n\n```bash\ncd ~/aios-projects/{name}\nsource .venv/bin/activate\njupyter lab\n```\n"
    )
    (project_dir / "requirements.txt").write_text("\n".join(tmpl["packages"]) + "\n")
    console.print("[green]✓[/green] Project structure created")

    if not no_venv:
        console.print("[dim]  Creating virtual environment...[/dim]", end="")
        code, _, err = run([py_cmd(), "-m", "venv", str(project_dir / ".venv")])
        if code == 0:
            console.print("\r[green]✓[/green] Virtual environment created (.venv)")
        else:
            console.print(f"\r[yellow]⚠[/yellow] venv failed: {err}")
            no_install = True

    if not no_install and tmpl["packages"]:
        venv_pip = project_dir / ".venv" / "bin" / "pip"
        pip_exec = str(venv_pip) if venv_pip.exists() else py_cmd()
        pip_args = [pip_exec, "install"] if pip_exec.endswith("pip") else [pip_exec, "-m", "pip", "install"]
        console.print(f"[dim]  Installing {len(tmpl['packages'])} package(s)...[/dim]")
        with Progress(SpinnerColumn(), TextColumn("{task.description}"), TimeElapsedColumn(), console=console) as p:
            task = p.add_task("pip install...", total=None)
            code, _, _ = run(pip_args + tmpl["packages"], timeout=300)
            p.stop()
        console.print("[green]✓[/green] Packages installed" if code == 0 else "[yellow]⚠[/yellow] pip had errors (install manually)")

    console.print()
    console.print(Panel(
        f"[bold white]{name}[/bold white] is ready!\n\n"
        f"[dim]Location:[/dim]  {project_dir}\n"
        f"[dim]Template:[/dim]  {template}\n\n"
        f"[bold cyan]Next steps:[/bold cyan]\n"
        f"  cd ~/aios-projects/{name}\n"
        f"  source .venv/bin/activate\n"
        f"  aios notebook",
        title="[bold green]✓ Project Created[/bold green]",
        border_style="green",
    ))


